/**
 * Excel-Upload-Friendly Data Structure for Feasibility Report Template
 * 
 * This schema defines the input data format that can be exported/imported
 * to Excel for the 개발사업 사전검토 보고서 (Preliminary Feasibility Review).
 * 
 * Sheet names and column structure match the frozen report template v1.0.
 * Do NOT modify PDF generation or screen layout - this is for data input only.
 */

// ============================================================================
// [기본정보] Basic Project Information
// ============================================================================
export interface 기본정보 {
  대상지주소: string
  대지면적_sqm: number
  대지면적_pyeong: number
  사업유형: string // e.g., "공동주택 신축사업"
  작성일: string // e.g., "2026년 4월 8일"
  문서번호: string // e.g., "AS-2026-0408-001"
}

// ============================================================================
// [대상지분석] Site Analysis
// ============================================================================
export interface 대상지분석 {
  소재지: string
  토지이용계획: string // e.g., "현장 확인 필요" or actual value
  접도현황: string // e.g., "현장 확인 필요" or actual value
  주변입지특성: string // optional description
}

// ============================================================================
// [법규검토] Regulation Review
// ============================================================================
export interface 법규검토 {
  용도지역: string // e.g., "제2종 일반주거지역"
  건폐율한도_percent: number // e.g., 60
  용적률한도_percent: number // e.g., 200
  높이제한: string // e.g., "별도 확인" or "25m"
  최대층수: number // e.g., 7
}

// ============================================================================
// [배치안비교] Layout Comparison
// ============================================================================
export interface 배치안항목 {
  배치안명: string // e.g., "배치안 A"
  건폐율: number // e.g., 45.2
  층수: number // e.g., 7
  세대수: number // e.g., 48
  주차대수: number // e.g., 58
  ROI: number // e.g., 18.5
}

export type 배치안비교 = 배치안항목[]

// ============================================================================
// [선택안상세] Selected Layout Details
// ============================================================================
export interface 선택안상세 {
  사용자선택배치안: string // e.g., "배치안 A"
  AI추천배치안: string // e.g., "배치안 A"
  선택안설명문구: string // e.g., "중정형 배치로 채광과 환기에 유리"
  세대수: number
  규모: string // e.g., "지상 7층"
  주차대수: number
  건폐율: number
  연면적_sqm: number
  연면적_pyeong: number
  용적률: number
  배치특성: string[] // e.g., ["중정형 배치", "남향 위주 세대 배치", "지하주차장", "옥상 조경"]
}

// ============================================================================
// [사업성검토] Financial Analysis
// ============================================================================
export interface 사업성검토 {
  토지비: number // in KRW
  토지비_억원: string // formatted, e.g., "45.0억원"
  공사비: number
  공사비_억원: string
  기타비용: number
  기타비용_억원: string
  총사업비: number
  총사업비_억원: string
  총분양수입: number
  총분양수입_억원: string
  예상사업이익: number
  예상사업이익_억원: string
  투자수익률_ROI: number // e.g., 18.5
  손익분기분양률: number // e.g., 84.6
  예상사업기간: string // e.g., "시공 18~24개월"
  총사업기간: string // e.g., "24~32개월"
}

// ============================================================================
// [리스크] Risk Factors
// ============================================================================
export interface 리스크항목 {
  구분: '토지 관련' | '인허가 관련' | '시장 관련' | '공사 관련'
  내용: string[]
}

export type 리스크 = 리스크항목[]

// ============================================================================
// [결론및제안] Conclusion and Recommendation
// ============================================================================
export type 종합의견등급 = '사업 추진 적합' | '조건부 추진 검토' | '추가 검토 필요'

export interface 결론및제안 {
  종합의견: 종합의견등급
  결론요약문구: string
  권고박스문구: string
}

// ============================================================================
// [고정설정] Fixed Template Settings (Read-Only)
// ============================================================================
export interface 고정설정 {
  브랜드명: string // "Archi-Scan | 건축기획 분석 시스템"
  영문부제: string // "PRELIMINARY FEASIBILITY REVIEW"
  PDF방식: string // "client-side jsPDF"
  한글폰트: string // "NotoSansKR-Regular.ttf"
}

// ============================================================================
// Complete Report Data Structure
// ============================================================================
export interface FeasibilityReportData {
  기본정보: 기본정보
  대상지분석: 대상지분석
  법규검토: 법규검토
  배치안비교: 배치안비교
  선택안상세: 선택안상세
  사업성검토: 사업성검토
  리스크: 리스크
  결론및제안: 결론및제안
  고정설정: 고정설정
}

